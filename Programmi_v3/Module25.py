""" Prendere solo alcune parti di una lista. """

# Definiamo sempre la lista e i suoi elementi.
allievi_4E = ["Gabriele", "Alessandro", "Anna", "Michela", "Antonio"]

# Andiamo a stampare il contenuto della lista, ma dalla posizione zero compresa alla posizione 4 compresa.
print (allievi_4E  [:4])

# Qui invece stampiamo dalla posizione numero tre fino all'ultima posizione.
print (allievi_4E  [3:])

# In questo caso invece visualizzeremo la prima e l'ultima posizione comprese.
print (allievi_4E  [:] )
